/**
 * Alchemist Canvas — synthesis planning workspace.
 *
 * Three-stage flow on a React Flow graph:
 *   Constraints + Target → NIM-grounded planner → Protocol + Predicted properties
 *
 * Each node renders calm, sentence-case labels. Real values come from the
 * backend (`/api/v2/alchemi/properties` and `/api/v2/alchemi/chat`); when
 * AI features are inactive (no NVIDIA_API_KEY) the planner returns curated
 * DB values and we surface that as the provenance ("Curated DB" rather
 * than "Generated by Llama-3.3-70B").
 */

