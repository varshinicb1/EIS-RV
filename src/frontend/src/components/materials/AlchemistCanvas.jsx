function ProtocolNode({ data }) {
  const [isPlaying, setIsPlaying] = useState(false);
  const formattedSteps = data.steps.map(s => ({
    action: s, phase: 'Synthesis', duration_min: 5,
    critical: /exotherm|pyroph|caution|critical/i.test(s),
  }));

  return (
    <NodeShell accent={COLORS.protocol} width={420}>
      <Slot accent={COLORS.protocol} />
      <NodeHeader icon={FlaskConical} accent={COLORS.protocol}
        title="Synthesis protocol"
        subtitle={data.material} />
      <div style={{ padding: '12px 14px' }}>
        {!isPlaying ? (
          <>
            <ol style={{ paddingLeft: 0, listStyle: 'none', margin: 0, display: 'flex', flexDirection: 'column', gap: 6 }}>
              {data.steps.map((s, i) => (
                <li key={i} style={{
                  display: 'flex', gap: 10,
                  padding: '8px 10px',
                  background: 'var(--bg-elevated)',
                  border: '1px solid var(--border-primary)',
                  borderRadius: 'var(--radius-sm)',
                  fontSize: 12, color: 'var(--text-primary)', lineHeight: 1.45,
                }}>
                  <span data-number style={{
                    color: 'var(--accent)', fontWeight: 500, minWidth: 18,
                  }}>
                    {(i + 1).toString().padStart(2, '0')}
                  </span>
                  <span>{s}</span>
                </li>
              ))}
            </ol>
            <button
              onClick={() => setIsPlaying(true)}
              style={primaryBtn}
              onMouseEnter={e => e.currentTarget.style.background = 'var(--accent-hover)'}
              onMouseLeave={e => e.currentTarget.style.background = 'var(--accent)'}
            >
              <Play size={13} fill="currentColor" /> Animate synthesis
            </button>
          </>
        ) : (
          <div style={{ display: 'flex', flexDirection: 'column', gap: 10 }}>
            <SynthesisAnimator protocolSteps={formattedSteps} materialAtoms={data.atoms} />
            <button onClick={() => setIsPlaying(false)} style={secondaryBtn}>Stop animation</button>
          </div>
        )}
      </div>
      <Dot accent={COLORS.protocol} />
    </NodeShell>
  );
}

